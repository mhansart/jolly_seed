<h3>Qu'est-ce que JollySeed ? </h3>

<p>Ramener de la biodiversité en ville et inversement, prendre soin de l'environnement, créer des liens sociaux, partager des connaissances, s'alimenter plus sainement, local et de saison...et si vous testiez le co-jardinage ?
    Bienvenue sur le réseau social JollySeed !
    Basé sur toute la partie bruxelloise et wallonne de Belgique et qui n'aspire qu'à grandir </p>

<h3>Description :</h3>

<p> Nous sommes une plateforme de communication entre personnes qui désirent donner des produits issus du jardin et d'autres qui désirent donner leur temps pour l'entretien ou la récolte.
    Le site n'est pas un site commercial. </p>

<h3>Comment l'application fonctionne t-elle ? </h3>

<p> Vous devez vous connecter pour pouvoir créer un compte et voir la totalité des annonces. Celles-ci sont catégorisées par thèmes, mots-clé mais surtout proximité avec les autres personnes ce qui vous permettra de donner et obtenir de l'aide de la manière la plus simple et optimale qui soit.
